
// Assignment operators in java

package basic_java;

public class Assignmentop {
	class Main {
		  public static void main(String[] args) {   //main method
		    
		    
		    int a = 4;    //create variables
		    int var;

		    var = a;     //assign value 
		    System.out.println("var using =: " + var);  

		    
		    var += a;          // assign value using =+
		    System.out.println("var using +=: " + var);

		   
		    var *= a;             // assign value using =*
		    System.out.println("var using *=: " + var);
		  }
		}

}
